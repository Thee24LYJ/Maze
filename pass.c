int simple()        //简单地图函数
{
    char a[20][20] = { "###########",
                    "#* # #   ##",
                    "# #    #  #",
                    "#  ## # # #",
                    "##    #   #",
                    "# ##### # #",
                    "##       ## ",
                    "# ###### ##",
                    "#      #  #",
                    "###### ####",
    };
    int i, x, y, p, q;
    char ch;
    x = 1; y = 1; p = 9; q = 6;
    for (i = 0; i <= 9; i++)
        puts(a[i]);
    while (x != p || y != q)
    {
        ch = getch();
        if (ch == 's')
        {
            if (a[x + 1][y] != '#')
            {
                a[x][y] = ' ';
                x++;
                a[x][y] = '*';
            }
        }
        if (ch == 'w')
        {
            if (a[x - 1][y] != '#')
            {
                a[x][y] = ' ';
                x--;
                a[x][y] = '*';
            }
        }
        if (ch == 'a')
        {
            if (a[x][y - 1] != '#')
            {
                a[x][y] = ' ';
                y--;
                a[x][y] = '*';
            }
        }
        if (ch == 'd')
        {
            if (a[x][y + 1] != '#')
            {
                a[x][y] = ' ';
                y++;
                a[x][y] = '*';
            }
        }
        system("cls");
        for (i = 0; i <= 9; i++)
            puts(a[i]);
    }
    return 0;
}
